          <p class="titel">algemeen</p><br>
          <p class="info">
            Sportclub Heerenveen is een Nederlandse profvoetbalclub uit Heerenveen, 
            die uitkomt in de Eredivisie van zowel de heren- als vrouwencompetitie. 
            De grootste successen die de club heeft behaald zijn het bereiken van 
            de groepsfase van de UEFA Champions League in 2000-2001 en het winnen 
            van de KNVB beker in 2009.<br><br>
            De thuisbasis van sc Heerenveen is het Abe Lenstra Stadion, 
            vernoemd naar de oud-voetballer Abe Lenstra die in 1985 overleed.
            Er kunnen momenteel 27.224 toeschouwers plaatsnemen in het 
            stadion. Dit betekent in theorie dat bijna alle inwoners van 
            de plaats Heerenveen (32.200 inwoners) als toeschouwer in het 
            stadion plaats kunnen nemen.
          </p><br><br><br>
          <p class="titel">Geschiedenis</p><br>
          <p class="info">
            In 1902 werd in Heerenveen gevoetbald door de club Victorie. 
            Er ontstonden verschillende elftallen en verenigingen die 
            tegen andere steden en dorpen uitkwamen. Door samengaan van 
            Victorie en de Heerenveense vereniging Achilles ontstond in 
            1908 de club V.A.C. Deze club ging in 1918 ter ziele wegens 
            de mobilisatie, echter was het een kwestie van tijd dat de 
            club opnieuw werd opgericht onder een andere naam.
          </p><br><br><br>
          <p class="titel">Oprichting</p><br>
          <p class="info">
            De club sc Heerenveen ontstond op 20 juli 1920 onder de 
            naam Athleta; in het seizoen 1921/22 werd de naam gewijzigd 
            in Spartaan. Toen de club circa een jaar later toetrad tot 
            de competitie van de Nederlandse Voetbal Bond, kreeg ze de 
            naam VV Heerenveen, omdat er al andere clubs waren, die 
            onder de naam (De) Spartaan speelden. Na splitsing in 
            afdelingen voor amateur- en betaald voetbal veranderde de 
            naam van de club op 1 juni 1977 in sc Heerenveen.
          </p><br><br><br>
          <p class="titel">Het stadion</p><br>
          <p class="info">
            Sinds 1994 speelt sc Heerenveen haar thuiswedstrijden in 
            het nieuwe Abe Lenstra stadion (huidige capaciteit ruim 
            26.100 plaatsen). Er is sinds dat jaar veel gebouwd, 
            verbouwd aan het stadion. Het bleek wegens het succes 
            van de club al gauw te klein voor onder andere supporters, 
            sponsors, personeel, kleedruimtes en technische staf.
          </p><br><br><br>
